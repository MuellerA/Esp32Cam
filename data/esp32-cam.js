var menuContent ;
var settings ;

////////////////////////////////////////////////////////////////////////////////
// menu
////////////////////////////////////////////////////////////////////////////////

function menu()
{
    menuContent.style.display = (menuContent.style.display == 'none') ? 'block' : 'none' ;
}

